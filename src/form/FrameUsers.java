    private void BtnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUbahActionPerformed
        try {
            if (TextIdUser.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pilih data yang ingin diubah terlebih dahulu");
            } else if (TextUsername.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Username tidak boleh kosong");
            } else if (TextPassword.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Password tidak boleh kosong");
            } else {
                crudObj.ubahUser(
                    TextIdUser.getText(),
                    TextUsername.getText(),
                    TextPassword.getText(),
                    ComboBoxRole.getSelectedItem().toString()
                );
                loadDataTabel();
                clearForm();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_BtnUbahActionPerformed

