    private void BtnHapusActionPerformed(java.awt.event.ActionEvent evt) {                                         
        try {
            if (TextIdUser.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus");
            } else {
                int Pilihan = JOptionPane.showConfirmDialog(null, 
                        "Apakah Anda yakin ingin menghapus data?", 
                        "Konfirmasi Hapus", 
                        JOptionPane.YES_NO_OPTION);
                
                if(Pilihan == JOptionPane.YES_OPTION) {
                    crudObj.hapusUser(TextIdUser.getText());
                    loadDataTabel();
                    clearForm();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }                                        

    private void TableMouseClicked(java.awt.event.MouseEvent evt) {                                   
        int baris = Table.getSelectedRow();
        
        String id_user = Table.getValueAt(baris, 0).toString();
        String username = Table.getValueAt(baris, 1).toString();
        String password = Table.getValueAt(baris, 2).toString();
        String role = Table.getValueAt(baris, 3).toString();
        
        TextIdUser.setText(id_user);
        TextUsername.setText(username);
        TextPassword.setText(password);
        ComboBoxRole.setSelectedItem(role);
    }                                  

    private void TextPencarianKeyReleased(java.awt.event.KeyEvent evt) {                                          
        String c = TextPencarian.getText();
        String sql = "select * from users where id_user like '%"+c+"%' "+
                    "or username like '%"+c+"%' or password like '%"+c+"%' or role like '%"+c+"%'";
        crudObj.loadData(Table, sql);
    }

