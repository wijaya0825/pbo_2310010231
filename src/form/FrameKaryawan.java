    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {                                          
        try {
            String idUser = getSelectedIdUser();
            if (idUser == null) {
                JOptionPane.showMessageDialog(null, "Pilih ID User terlebih dahulu");
            } else if (TextNamaKaryawan.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nama Karyawan tidak boleh kosong");
            } else {
                crudObj.simpanKaryawan(
                    idUser, 
                    TextNamaKaryawan.getText(), 
                    TextEmail.getText(), 
                    TextAlamat.getText(), 
                    TextNoTelpon.getText()
                );
                loadDataTabel();
                clearForm();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }                                         

    private void BtnUbahActionPerformed(java.awt.event.ActionEvent evt) {                                        
        try {
            String idUser = getSelectedIdUser();
            if (TextIdKaryawan.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pilih data yang ingin diubah");
            } else if (idUser == null) {
                JOptionPane.showMessageDialog(null, "Pilih ID User terlebih dahulu");
            } else {
                crudObj.ubahKaryawan(
                    TextIdKaryawan.getText(),
                    idUser, 
                    TextNamaKaryawan.getText(), 
                    TextEmail.getText(), 
                    TextAlamat.getText(), 
                    TextNoTelpon.getText()
                );
                loadDataTabel();
                clearForm();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }                                       

    private void BtnHapusActionPerformed(java.awt.event.ActionEvent evt) {                                         
        try {
            if (TextIdKaryawan.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus");
            } else {
                int Pilihan = JOptionPane.showConfirmDialog(null, "Yakin hapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
                if(Pilihan == JOptionPane.YES_OPTION) {
                    crudObj.hapusKaryawan(TextIdKaryawan.getText());
                    loadDataTabel();
                    clearForm();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }                                        

    private void BtnClearActionPerformed(java.awt.event.ActionEvent evt) {                                         
        clearForm();
    }                                        

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                     
        int baris = jTable1.getSelectedRow();
        
        String id_karyawan = jTable1.getValueAt(baris, 0).toString();
        String id_user = jTable1.getValueAt(baris, 1).toString();
        String nama = jTable1.getValueAt(baris, 2).toString();
        String email = jTable1.getValueAt(baris, 3) != null ? jTable1.getValueAt(baris, 3).toString() : "";
        String alamat = jTable1.getValueAt(baris, 4) != null ? jTable1.getValueAt(baris, 4).toString() : "";
        String no_telpon = jTable1.getValueAt(baris, 5) != null ? jTable1.getValueAt(baris, 5).toString() : "";
        
        TextIdKaryawan.setText(id_karyawan);
        TextNamaKaryawan.setText(nama);
        TextEmail.setText(email);
        TextAlamat.setText(alamat);
        TextNoTelpon.setText(no_telpon);
        
        for (int i = 0; i < ComboBoxIdUser.getItemCount(); i++) {
            if (ComboBoxIdUser.getItemAt(i).startsWith(id_user + " - ")) {
                ComboBoxIdUser.setSelectedIndex(i);
                break;
            }
        }
    }                                    

    private void TextPencarianKeyReleased(java.awt.event.KeyEvent evt) {                                          
        String c=TextPencarian.getText();
        String cari="select * from karyawan where id_karyawan like '%"+c+"%' "+
                    "or id_user like '%"+c+"%' or nama_karyawan like '%"+c+"%' or email like '%"+c+"%' or alamat_karyawan like '%"+c+"%' or no_telpon like '%"+c+"%'";
        crudObj.loadData(jTable1, cari);
    }
    
