    public void loadIdUserComboBox() {
        try {
            ComboBoxIdUser.removeAllItems();
            
            ResultSet rs = crudObj.koneksiDB.createStatement().executeQuery("SELECT id_user, username FROM users");

            ComboBoxIdUser.addItem("Pilih ID User");
            
            while (rs.next()) {
                ComboBoxIdUser.addItem(rs.getString("id_user") + " - " + rs.getString("username"));
            }
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal memuat ID User: " + e.getMessage());
        }
    }
    
    public String getSelectedIdUser() {
        if (ComboBoxIdUser.getSelectedIndex() == 0) {
            return null;
        }

        String selectedItem = ComboBoxIdUser.getSelectedItem().toString();
        String idUser = selectedItem.split(" ")[0];
        return idUser;
    }


